//•	https:// 
//Indica que a comunicação entre o cliente e o servidor será feita por meio
//de uma conexão segura com o protocolo HTTPS (HyperText Transfer Protocol Secure).
//O HTTPS adiciona uma camada de segurança criptografada para proteger a transferência
//de dados entre o cliente e o servidor.
//Utilizando uma criptografia para proteger os dados transmitidos,
//garantindo que as informações não sejam interceptadas ou modificadas por terceiros na transferência.

//•	minhaapi.com.br 
//Essa parte indica o domínio da API. Um endereço legível que é usado para acessar recursos na internet.
//O ".com.br" é um domínio de nível superior (TLD) que pode indicar uma organização baseada no Brasil.

//•	/api/vendas 
//Parte do caminho do URL da API. O caminho do URL especifica a localização do recurso que deseja acessar dentro do domínio.

